vim.cmd [[
  function! RunSubstitutions()
    execute "'<,'>s/\\s*$//g"
    execute "'<,'>s/\"/'/g"
    execute "'<,'>s/:\\([^= ]*\\)\\s*=>\\s*/\\1: /g"
    execute "'<,'>s/{\\([^ ].*[^ ]\\)}/{ \\1 }/g"
    execute "'<,'>s/^\\(\\s*\\)+/\\1/g"
    execute "'<,'>s/\\[/ /g"
    execute "'<,'>s/\\]/,/g"
  endfunction
]]
vim.api.nvim_set_keymap('v', '<Leader>r', ':<C-u>call RunSubstitutions()<CR>', { noremap = true, silent = true })

